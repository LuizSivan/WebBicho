import {NestFactory} from '@nestjs/core';
import {AppModule} from './app.module';
import {
  INestApplication,
  ValidationPipe
} from '@nestjs/common';
import {createDatabaseIfNotExist} from './core/database/maintenance';
import dotenv from 'dotenv';
import * as process from 'process';
import {
  DocumentBuilder,
  OpenAPIObject,
  SwaggerModule
} from '@nestjs/swagger';

dotenv.config();
const PORT: string | number = process.env.PORT || 8070;
export const HOST: string = process.env.HOST || 'http://127.0.0.1';

async function bootstrap(): Promise<void> {
  await createDatabaseIfNotExist();
  const app: INestApplication = await NestFactory.create(AppModule);
  app.enableCors({
    origin: '*',
    optionsSuccessStatus: 204
  });
  
  const config: Omit<OpenAPIObject, 'paths'> = new DocumentBuilder()
      .setTitle('API WebBicho')
      .setDescription('Lista de todas as rotas disponíveis para a API WebBicho.')
      .setVersion('1.0')
      .build();
  const document: OpenAPIObject = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api', app, document);
  
  app.useGlobalPipes(new ValidationPipe({whitelist: true}));
  await app.listen(PORT);
}

